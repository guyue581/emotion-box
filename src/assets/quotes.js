// 根据情绪获取随机语录
export function getRandomQuote(emotion) {
  const quotesList = quotes[emotion]
  if (!quotesList || quotesList.length === 0) {
    // 如果没有对应情绪的语录，返回通用语录
    const allQuotes = Object.values(quotes).flat()
    return allQuotes[Math.floor(Math.random() * allQuotes.length)]
  }
  return quotesList[Math.floor(Math.random() * quotesList.length)]
}

// 获取所有情绪类型
export function getAllEmotions() {
  return Object.keys(emotionMap).map(key => ({
    key,
    ...emotionMap[key]
  }))
}
